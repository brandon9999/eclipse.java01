import java.io.*; 

public class FileStream4
{     
	public static void main(String[] args) throws IOException
	{         
		FileInputStream fis = new FileInputStream("D:\\temp3\\a.txt");          
		int i;         
		while( (i=fis.read()) != -1 )
		{              
			System.out.print((char)i);          
		}         
		fis.close();     
	}  
} 