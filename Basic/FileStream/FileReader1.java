import java.io.*; 

public class FileReader1
{     
	public static void main(String[] args) throws IOException
	{         
		FileReader fr = new FileReader("D:\\temp3\\test2.txt");          
		int i;         
		while( (i=fr.read()) != -1 )
		{              
			System.out.print((char)i);          
		}         
		fr.close();     
	}  
} 